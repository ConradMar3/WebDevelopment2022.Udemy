// Adding a class of 'invisible' to "btn". CSS = .hidden { visibility: hidden };
document.querySelector("button").classList.add("invisible");

// Can also use '.remove' to "btn";
document.querySelector("button").classList.remove("invisible");

// '.toggle' to class add - "invisible", removes "invisible" when visible and adds "invisible" when not visible;
document.querySelector("button").classList.toggle("invisible");
